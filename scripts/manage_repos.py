def _die(msg: str) -> None:
    print(f"ERROR: {msg}", file=sys.stderr)
    sys.exit(1)


def _parse_filter(flag: str, value: str) -> list[str]:
    """Parse a filter flag value.

    'all'         → [] (no filter — match everything)
    'a,b,c'       → ['a', 'b', 'c']
    '' or ','     → error (empty value not allowed)
    'all,foo'     → error (cannot mix 'all' with specific values)
    """
    stripped = value.strip()
    if not stripped:
        _die(f"{flag} requires a non-empty value")
    if stripped.lower() == "all":
        return []
    parts = [p.strip() for p in stripped.split(",") if p.strip()]
    if not parts:
        _die(f"{flag}: invalid value '{value}' — use 'all' or a comma-separated list")
    if any(p.lower() == "all" for p in parts):
        _die(f"{flag}: cannot mix 'all' with specific values")
    return parts


def _print_usage() -> None:
    print("""
Usage: python3 scripts/manage_repos.py <subcommand> [options]

Subcommands:
  clone   [filters] [--full] [--git-name N] [--git-email E]
  update  [filters] [--parallel N]
  fetch   [filters] [--parallel N]
  status  [filters]
  branch create  <name> [filters] [--dry-run]
  branch checkout <name> [filters] [--fallback] [--dry-run]
  branch delete  <name> [filters] [--force] [--dry-run]
  stash   [filters] [--dry-run]
  stash pop [filters] [--dry-run]
  reset   [filters] [--hard] [--dry-run]
  run     <git-or-shell-cmd...> [filters] [--parallel N] [--dry-run]
  cleanup [--force] [--dry-run]

Filters (accepted by all subcommands):
  --domains <list|all>   Comma-separated domain slugs, or 'all' (e.g. networking,compute)
  --types <list|all>     Comma-separated types, or 'all' (res,ptn,utl)
  --modules <list|all>   Comma-separated module names, or 'all'
  --domain <slug>        Alias for --domains (single value)
  --type   <res|ptn|utl> Alias for --types (single value)
  --module <name>        Alias for --modules (single value, backward compat)

Global options:
  --dry-run              Print what would happen without executing
  -h, --help             Show this help
""")


class Args:
    """Simple parsed arguments container."""
    subcommand: str = ""
    sub2: str = ""
    branch_name: str = ""
    domains: list[str]
    types: list[str]
    modules: list[str]
    dry_run: bool = False
    full: bool = False
    git_name: str = ""
    git_email: str = ""
    parallel: int = 0
    fallback: bool = False
    force: bool = False
    hard: bool = False
    stale_threshold: int = _DEFAULT_STALE_THRESHOLD
    run_cmd: list[str] = []


def parse_args(argv: list[str]) -> Args:
    a = Args()
    a.domains = []
    a.types = []
    a.modules = []
    a.run_cmd = []
    it = iter(argv)

    def nextval(flag: str) -> str:
        try:
            return next(it)
        except StopIteration:
            _die(f"{flag} requires a value")
            return ""

    first = True
    for token in it:
        if first:
            first = False
            if token in ("-h", "--help", "help"):
                _print_usage()
                sys.exit(0)
            a.subcommand = token
            # branch and stash have a second positional token
            if token in ("branch", "stash"):
                sub2 = next(it, "")
                if not sub2:
                    _die(f"'{token}' requires a sub-operation (e.g. create/checkout/delete or pop)")
                a.sub2 = sub2
                # branch also needs a name
                if token == "branch":
                    name = next(it, "")
                    if not name or name.startswith("--"):
                        _die("'branch' requires a branch name after the operation")
                    a.branch_name = name
            continue

        if a.subcommand == "run" and not token.startswith("--"):
            # collect remaining non-flag tokens as the run command
            a.run_cmd.append(token)
            continue

        if token == "--domains":
            a.domains = _parse_filter("--domains", nextval("--domains"))
        elif token.startswith("--domains="):
            a.domains = _parse_filter("--domains", token.split("=", 1)[1])
        elif token == "--domain":
            # backward-compat alias: single value treated as a one-element list
            a.domains = [nextval("--domain")]
        elif token.startswith("--domain="):
            a.domains = [token.split("=", 1)[1]]
        elif token == "--types":
            a.types = _parse_filter("--types", nextval("--types"))
        elif token.startswith("--types="):
            a.types = _parse_filter("--types", token.split("=", 1)[1])
        elif token == "--type":
            # backward-compat alias: single value
            a.types = [nextval("--type")]
        elif token.startswith("--type="):
            a.types = [token.split("=", 1)[1]]
        elif token == "--modules":
            a.modules = _parse_filter("--modules", nextval("--modules"))
        elif token.startswith("--modules="):
            a.modules = _parse_filter("--modules", token.split("=", 1)[1])
        elif token == "--module":
            # backward-compat alias: single value
            a.modules = [nextval("--module")]
        elif token.startswith("--module="):
            a.modules = [token.split("=", 1)[1]]
        elif token == "--parallel":
            a.parallel = int(nextval("--parallel"))
        elif token.startswith("--parallel="):
            a.parallel = int(token.split("=", 1)[1])
        elif token == "--git-name":
            a.git_name = nextval("--git-name")
        elif token.startswith("--git-name="):
            a.git_name = token.split("=", 1)[1]
        elif token == "--git-email":
            a.git_email = nextval("--git-email")
        elif token.startswith("--git-email="):
            a.git_email = token.split("=", 1)[1]
        elif token == "--dry-run":
            a.dry_run = True
        elif token == "--full":
            a.full = True
        elif token == "--fallback":
            a.fallback = True
        elif token == "--force":
            a.force = True
        elif token == "--hard":
            a.hard = True
        elif token == "--stale-threshold":
            try:
                a.stale_threshold = int(nextval("--stale-threshold"))
            except (ValueError, SystemExit):
                _die("--stale-threshold requires an integer value")
        elif token.startswith("--stale-threshold="):
            try:
                a.stale_threshold = int(token.split("=", 1)[1])
            except ValueError:
                _die("--stale-threshold requires an integer value")
        elif token == "-h" or token == "--help":
            _print_usage()
            sys.exit(0)
        elif a.subcommand == "run":
            a.run_cmd.append(token)
        else:
            _die(f"Unknown option: {token}")

    return a


def _prompt_git_identity(git_name: str, git_email: str) -> tuple[str, str]:
    """Prompt for git user.name / user.email when not supplied via flags.

    Identity is always set explicitly in each repo's local config — global git
    config is intentionally ignored.  Press Enter to leave a field blank (no
    local config written for that field).  Skips the prompt when stdin is not a
    TTY (non-interactive run).
    """
    if not sys.stdin.isatty():
        return git_name, git_email

    needs_prompt = not git_name or not git_email
    if not needs_prompt:
        return git_name, git_email

    print()
    print("  Git identity (set explicitly in each repo — Enter to skip a field):")

    if not git_name:
        try:
            git_name = input("  user.name : ").strip()
        except EOFError:
            git_name = ""

    if not git_email:
        try:
            git_email = input("  user.email: ").strip()
        except EOFError:
            git_email = ""

    print()
    return git_name, git_email


