def _do_update(mod: dict, dry_run: bool) -> tuple[str, str, str]:
    """Returns (status, label, note) — runs in a thread."""
    label = _label(mod)
    if not is_cloned(mod):
        return ("skip", label, "not cloned")
    if dry_run:
        return ("dry", label, "")
    result = _git(mod, ["pull", "--ff-only", "--quiet"], check=False)
    if result.returncode == 0:
        return ("ok", label, "")
    note = (result.stderr or result.stdout).strip().splitlines()
    return ("fail", label, note[-1] if note else "pull failed")


def cmd_update(
    modules: list[dict],
    parallel: int = 1,
    dry_run: bool = False,
) -> int:
    cloneable = modules
    print(f"\nAVM update — {len(cloneable)} modules  [parallel: {parallel}]")
    print(SEP)

    ok_n = skip_n = fail_n = 0
    results = _run_parallel(_do_update, cloneable, parallel, dry_run)

    for status, label, note in results:
        if status == "ok":
            print(f"  {ok('✓')}  PULL     {label}")
            ok_n += 1
        elif status == "skip":
            print(f"  {dim('-')}  SKIP     {label}  {dim(note)}")
            skip_n += 1
        elif status == "dry":
            print(f"  {dim('~')}  PULL     {label}  {dim('dry-run')}")
            ok_n += 1
        else:
            print(f"  {err('✗')}  FAIL     {label}  {dim(note)}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


