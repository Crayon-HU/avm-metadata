_USE_COLOR = sys.stdout.isatty()


def _c(code: str, text: str) -> str:
    return f"\033[{code}m{text}\033[0m" if _USE_COLOR else text


def ok(s: str) -> str:   return _c("32", s)
def warn(s: str) -> str: return _c("33", s)
def err(s: str) -> str:  return _c("31", s)
def dim(s: str) -> str:  return _c("2", s)


# ---------------------------------------------------------------------------
# modules.yaml parser — line-by-line, stdlib only
# ---------------------------------------------------------------------------

def _strip(s: str) -> str:
    return s.strip().strip('"').strip("'")


def load_modules(
    filter_domains: list[str] | None = None,
    filter_types:   list[str] | None = None,
    filter_modules: list[str] | None = None,
) -> list[dict]:
    """
    Parse .config/modules.yaml and return module dicts matching the given filters.

    Each dict has keys: name, domain, type, url, branch.
    filter_domains / filter_types / filter_modules: if empty/None → no filter applied.
    """
    if not os.path.isfile(MODULES_FILE):
        _die(
            f".config/modules.yaml not found.\n"
            f"Run './avm.sh setup --domains all' first."
        )

    modules: list[dict] = []
    cur: dict = {}

    with open(MODULES_FILE, encoding="utf-8") as f:
        for raw in f:
            line = raw.rstrip("\r\n")
            # skip comments and blank lines
            stripped = line.lstrip()
            if not stripped or stripped.startswith("#"):
                continue

            if "- name:" in line:
                if cur.get("name") and cur.get("url"):
                    _maybe_add(modules, cur, filter_domains, filter_types, filter_modules)
                cur = {"name": _field(line, "name"), "branch": "main",
                       "domain": "", "type": "", "url": ""}
            elif cur and "domain:" in line and "name:" not in line:
                cur["domain"] = _field(line, "domain")
            elif cur and "type:" in line:
                cur["type"] = _field(line, "type")
            elif cur and "url:" in line:
                cur["url"] = _field(line, "url")
            elif cur and "branch:" in line:
                cur["branch"] = _field(line, "branch")

    if cur.get("name") and cur.get("url"):
        _maybe_add(modules, cur, filter_domains, filter_types, filter_modules)

    return modules


def _field(line: str, key: str) -> str:
    """Extract value after 'key:' from a YAML line."""
    idx = line.find(f"{key}:")
    if idx == -1:
        return ""
    return _strip(line[idx + len(key) + 1:])


def _maybe_add(
    modules: list[dict],
    cur: dict,
    filter_domains: list[str] | None,
    filter_types:   list[str] | None,
    filter_modules: list[str] | None,
) -> None:
    if filter_domains and cur.get("domain") not in filter_domains:
        return
    if filter_types and cur.get("type") not in filter_types:
        return
    if filter_modules and cur.get("name") not in filter_modules:
        return
    modules.append(dict(cur))


# ---------------------------------------------------------------------------
# Git helpers
# ---------------------------------------------------------------------------

def repo_dir(mod: dict) -> str:
    return os.path.join(REPO_ROOT, mod["name"])


def is_cloned(mod: dict) -> bool:
    return os.path.isdir(os.path.join(repo_dir(mod), ".git"))


def _git(mod: dict, args: list[str], check: bool = True) -> subprocess.CompletedProcess:
    """Run a git command in a module's repo directory."""
    return subprocess.run(
        ["git", "-C", repo_dir(mod)] + args,
        capture_output=True,
        text=True,
        check=check,
    )


def _git_clone(mod: dict, depth: int = 1) -> subprocess.CompletedProcess:
    """Clone a repo into the workspace root."""
    cmd = ["git", "clone"]
    if depth:
        cmd += ["--depth", str(depth)]
    cmd += ["--branch", mod["branch"], mod["url"], repo_dir(mod)]
    return subprocess.run(cmd, capture_output=True, text=True)


# ---------------------------------------------------------------------------
# Output helpers
# ---------------------------------------------------------------------------

SEP = "─" * 60


def _label(mod: dict) -> str:
    return f"{mod['name']}  [{mod['type']}] ({mod['domain']})"


def _print_result(
    symbol: str,
    verb: str,
    mod: dict,
    note: str = "",
) -> None:
    note_str = f"  {dim(note)}" if note else ""
    print(f"  {symbol}  {verb:<8}  {_label(mod)}{note_str}")


def _summary(ok_n: int, skip_n: int, fail_n: int) -> None:
    print(SEP)
    parts = [f"ok: {ok_n}", f"skipped: {skip_n}"]
    if fail_n:
        parts.append(err(f"failed: {fail_n}"))
    print("Done — " + ", ".join(parts))


