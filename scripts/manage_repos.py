def main() -> None:
    if len(sys.argv) < 2:
        _print_usage()
        sys.exit(1)

    a = parse_args(sys.argv[1:])

    if not a.subcommand:
        _print_usage()
        sys.exit(1)

    if a.types:
        invalid = [t for t in a.types if t not in ALL_TYPES]
        if invalid:
            _die(f"--types: invalid value(s): {', '.join(invalid)}. Must be one of: {', '.join(ALL_TYPES)}")

    # cleanup compares against all configured modules — scope filters don't apply
    if a.subcommand == "cleanup":
        if a.domains or a.types or a.modules:
            print(
                "  NOTE: --domains/--types/--modules are ignored by cleanup "
                "(orphaned repos have no catalog metadata to filter on)."
            )
        all_modules = load_modules()
        sys.exit(cmd_cleanup(all_modules, dry_run=a.dry_run, force=a.force))

    modules = load_modules(filter_domains=a.domains, filter_types=a.types, filter_modules=a.modules)

    if not modules:
        msg = "No modules match the specified filters."
        if a.domains:
            msg += f"  --domains={','.join(a.domains)}"
        if a.types:
            msg += f"  --types={','.join(a.types)}"
        if a.modules:
            msg += f"  --modules={','.join(a.modules)}"
        print(msg)
        sys.exit(0)

    # Dispatch
    sub = a.subcommand
    rc = 0

    if sub == "clone":
        rc = cmd_clone(modules, full=a.full, git_name=a.git_name,
                       git_email=a.git_email, dry_run=a.dry_run)

    elif sub == "update":
        parallel = a.parallel or 1
        rc = cmd_update(modules, parallel=parallel, dry_run=a.dry_run)

    elif sub == "fetch":
        parallel = a.parallel or 20
        rc = cmd_fetch(modules, parallel=parallel, dry_run=a.dry_run)

    elif sub == "status":
        parallel = a.parallel or 20
        rc = cmd_status(modules, parallel=parallel, dry_run=a.dry_run,
                        stale_threshold=a.stale_threshold)

    elif sub == "branch":
        op = a.sub2
        if op == "create":
            rc = cmd_branch_create(modules, a.branch_name, dry_run=a.dry_run)
        elif op == "checkout":
            rc = cmd_branch_checkout(modules, a.branch_name,
                                     fallback=a.fallback, dry_run=a.dry_run)
        elif op == "delete":
            rc = cmd_branch_delete(modules, a.branch_name,
                                   force=a.force, dry_run=a.dry_run)
        else:
            _die(f"Unknown branch operation '{op}'. Use: create, checkout, delete")

    elif sub == "stash":
        if a.sub2 == "pop":
            rc = cmd_stash_pop(modules, dry_run=a.dry_run)
        else:
            rc = cmd_stash(modules, dry_run=a.dry_run)

    elif sub == "reset":
        rc = cmd_reset(modules, hard=a.hard, dry_run=a.dry_run)

    elif sub == "run":
        parallel = a.parallel or 1
        rc = cmd_run(modules, a.run_cmd, parallel=parallel, dry_run=a.dry_run)

    else:
        _die(f"Unknown subcommand '{sub}'. Run 'manage_repos.py help' for usage.")

    sys.exit(rc)


if __name__ == "__main__":
    main()
