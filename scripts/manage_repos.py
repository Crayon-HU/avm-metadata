def cmd_clone(
    modules: list[dict],
    full: bool = False,
    git_name: str = "",
    git_email: str = "",
    dry_run: bool = False,
) -> int:
    depth = 0 if full else 1
    mode = "full history" if full else "shallow (--depth 1)"

    if not dry_run:
        git_name, git_email = _prompt_git_identity(git_name, git_email)

    print(f"\nAVM clone — {len(modules)} modules  [{mode}]")
    print(SEP)

    ok_n = skip_n = fail_n = 0

    for mod in modules:
        if is_cloned(mod):
            _print_result(dim("-"), "SKIP", mod, "already cloned")
            skip_n += 1
            # still apply identity config even on skip
            if not dry_run:
                if git_name:
                    _git(mod, ["config", "user.name", git_name], check=False)
                if git_email:
                    _git(mod, ["config", "user.email", git_email], check=False)
            continue

        if dry_run:
            _print_result(dim("~"), "CLONE", mod, "dry-run")
            ok_n += 1
            continue

        print(f"  ↓  CLONE    {_label(mod)}")
        result = _git_clone(mod, depth=depth)
        if result.returncode == 0:
            if git_name:
                _git(mod, ["config", "user.name", git_name], check=False)
            if git_email:
                _git(mod, ["config", "user.email", git_email], check=False)
            ok_n += 1
        else:
            stderr = result.stderr.strip().splitlines()
            note = stderr[-1] if stderr else "clone failed"
            _print_result(err("✗"), "FAIL", mod, note)
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


