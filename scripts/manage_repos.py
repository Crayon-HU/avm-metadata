#!/usr/bin/env python3
"""manage_repos.py — Multi-repo git operations across AVM module workspace.

Reads .config/modules.yaml (generated by scripts/generate_config.py) and runs
git operations across all matched repos. Replaces clone_repos.sh/.ps1 and
update_repos.sh/.ps1 while adding fetch, status, branch management, stash,
reset, and arbitrary command pass-through.

Network-bound operations (fetch, update/pull) support parallel execution via
--parallel N to avoid waiting ~200-2000 ms per repo sequentially.

Usage:
    python3 scripts/manage_repos.py clone   [filters] [--full] [--git-name N] [--git-email E]
    python3 scripts/manage_repos.py update  [filters] [--parallel N]
    python3 scripts/manage_repos.py fetch   [filters] [--parallel N]
    python3 scripts/manage_repos.py status  [filters]
    python3 scripts/manage_repos.py branch create <name>    [filters]
    python3 scripts/manage_repos.py branch checkout <name>  [filters] [--fallback]
    python3 scripts/manage_repos.py branch delete <name>    [filters] [--force]
    python3 scripts/manage_repos.py stash   [filters]
    python3 scripts/manage_repos.py stash pop [filters]
    python3 scripts/manage_repos.py reset   [filters] [--hard]
    python3 scripts/manage_repos.py run <cmd...> [-- filters]

Filters (accepted by all subcommands):
    --domain <name>        Filter by catalog domain slug (alias for --domains)
    --type <res|ptn|utl>   Filter by module type (alias for --types)
    --domains <list>       Comma-separated domain slugs (e.g. networking,compute)
    --types <list>         Comma-separated types (res,ptn,utl)
    --module <name>        Filter to a single module by name
    --dry-run              Print actions without executing

    ./avm.sh clone  →  python3 manage_repos.py clone
    ./avm.sh update →  python3 manage_repos.py update
"""

