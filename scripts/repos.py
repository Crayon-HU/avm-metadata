# ---------------------------------------------------------------------------
# Subcommand: status
# ---------------------------------------------------------------------------

def _do_status(mod: dict, _dry_run: bool) -> tuple[str, str, str]:
    """
    Returns (status, label, note).
    status: 'clean' | 'dirty' | 'behind' | 'skip' | 'fail'
    """
    label = _label(mod)
    if not is_cloned(mod):
        return ("skip", label, "not cloned")

    # Check for uncommitted changes
    st = _git(mod, ["status", "--porcelain"], check=False)
    if st.returncode != 0:
        return ("fail", label, "git status failed")
    dirty = bool(st.stdout.strip())

    # Check how far behind/ahead of upstream
    ahead_behind = _git(
        mod,
        ["rev-list", "--count", "--left-right", "@{u}...HEAD"],
        check=False,
    )
    behind = ahead = 0
    if ahead_behind.returncode == 0 and ahead_behind.stdout.strip():
        parts = ahead_behind.stdout.strip().split()
        if len(parts) == 2:
            behind, ahead = int(parts[0]), int(parts[1])

    if dirty:
        note = f"dirty{f', {behind}↓ {ahead}↑' if behind or ahead else ''}"
        return ("dirty", label, note)
    if behind or ahead:
        return ("behind", label, f"{behind}↓ {ahead}↑")
    return ("clean", label, "")


def cmd_status(modules: list[dict], parallel: int = 20, dry_run: bool = False) -> int:
    print(f"\nAVM status — {len(modules)} modules")
    print(SEP)

    results = _run_parallel(_do_status, modules, parallel, dry_run)

    clean_n = dirty_n = behind_n = skip_n = fail_n = 0
    for status, label, note in results:
        if status == "clean":
            # only print non-clean unless -v
            clean_n += 1
        elif status == "dirty":
            print(f"  {warn('!')}  DIRTY    {label}  {warn(note)}")
            dirty_n += 1
        elif status == "behind":
            print(f"  {warn('↓')}  BEHIND   {label}  {warn(note)}")
            behind_n += 1
        elif status == "skip":
            print(f"  {dim('-')}  SKIP     {label}  {dim(note)}")
            skip_n += 1
        else:
            print(f"  {err('✗')}  FAIL     {label}  {err(note)}")
            fail_n += 1

    print(SEP)
    print(
        f"Clean: {clean_n}  |  Dirty: {dirty_n}  |  "
        f"Behind/ahead: {behind_n}  |  Not cloned: {skip_n}"
    )
    return 0


