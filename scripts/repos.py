#!/usr/bin/env python3
"""repos.py — Multi-repo git operations across AVM module workspace.

Reads .config/modules.yaml (generated by scripts/generate_config.py) and runs
git operations across all matched repos. Replaces clone_repos.sh/.ps1 and
update_repos.sh/.ps1 while adding fetch, status, branch management, stash,
reset, and arbitrary command pass-through.

Network-bound operations (fetch, update/pull) support parallel execution via
--parallel N to avoid waiting ~200-2000 ms per repo sequentially.

Usage:
    python3 scripts/repos.py clone   [filters] [--full] [--git-name N] [--git-email E]
    python3 scripts/repos.py update  [filters] [--parallel N]
    python3 scripts/repos.py fetch   [filters] [--parallel N]
    python3 scripts/repos.py status  [filters]
    python3 scripts/repos.py branch create <name>    [filters]
    python3 scripts/repos.py branch checkout <name>  [filters] [--fallback]
    python3 scripts/repos.py branch delete <name>    [filters] [--force]
    python3 scripts/repos.py stash   [filters]
    python3 scripts/repos.py stash pop [filters]
    python3 scripts/repos.py reset   [filters] [--hard]
    python3 scripts/repos.py run <cmd...> [-- filters]

Filters (accepted by all subcommands):
    --domain <name>        Filter by catalog domain slug (alias for --domains)
    --type <res|ptn|utl>   Filter by module type (alias for --types)
    --domains <list>       Comma-separated domain slugs (e.g. networking,compute)
    --types <list>         Comma-separated types (res,ptn,utl)
    --module <name>        Filter to a single module by name
    --dry-run              Print actions without executing

    ./avm.sh clone  →  python3 repos.py clone
    ./avm.sh update →  python3 repos.py update
"""

import os
import subprocess
import sys
import tempfile
from concurrent.futures import ThreadPoolExecutor, as_completed

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
SCRIPT_DIR   = os.path.dirname(os.path.abspath(__file__))
REPO_ROOT    = os.path.dirname(SCRIPT_DIR)
MODULES_FILE = os.path.join(REPO_ROOT, ".config", "modules.yaml")

ALL_TYPES = ("res", "ptn", "utl")

# ---------------------------------------------------------------------------
# ANSI colours (no external libs)
# ---------------------------------------------------------------------------
_USE_COLOR = sys.stdout.isatty()


def _c(code: str, text: str) -> str:
    return f"\033[{code}m{text}\033[0m" if _USE_COLOR else text


def ok(s: str) -> str:   return _c("32", s)
def warn(s: str) -> str: return _c("33", s)
def err(s: str) -> str:  return _c("31", s)
def dim(s: str) -> str:  return _c("2", s)


# ---------------------------------------------------------------------------
# modules.yaml parser — line-by-line, stdlib only
# ---------------------------------------------------------------------------

def _strip(s: str) -> str:
    return s.strip().strip('"').strip("'")


def load_modules(
    filter_domains: list[str] | None = None,
    filter_types:   list[str] | None = None,
    filter_name:    str = "",
) -> list[dict]:
    """
    Parse .config/modules.yaml and return module dicts matching the given filters.

    Each dict has keys: name, domain, type, url, branch.
    filter_domains / filter_types: if empty/None → no filter applied.
    filter_name: if non-empty → match exact module name.
    """
    if not os.path.isfile(MODULES_FILE):
        _die(
            f".config/modules.yaml not found.\n"
            f"Run './avm.sh setup --domains all' first."
        )

    modules: list[dict] = []
    cur: dict = {}

    with open(MODULES_FILE, encoding="utf-8") as f:
        for raw in f:
            line = raw.rstrip("\r\n")
            # skip comments and blank lines
            stripped = line.lstrip()
            if not stripped or stripped.startswith("#"):
                continue

            if "- name:" in line:
                if cur.get("name") and cur.get("url"):
                    _maybe_add(modules, cur, filter_domains, filter_types, filter_name)
                cur = {"name": _field(line, "name"), "branch": "main",
                       "domain": "", "type": "", "url": ""}
            elif cur and "domain:" in line and "name:" not in line:
                cur["domain"] = _field(line, "domain")
            elif cur and "type:" in line:
                cur["type"] = _field(line, "type")
            elif cur and "url:" in line:
                cur["url"] = _field(line, "url")
            elif cur and "branch:" in line:
                cur["branch"] = _field(line, "branch")

    if cur.get("name") and cur.get("url"):
        _maybe_add(modules, cur, filter_domains, filter_types, filter_name)

    return modules


def _field(line: str, key: str) -> str:
    """Extract value after 'key:' from a YAML line."""
    idx = line.find(f"{key}:")
    if idx == -1:
        return ""
    return _strip(line[idx + len(key) + 1:])


def _maybe_add(
    modules: list[dict],
    cur: dict,
    filter_domains: list[str] | None,
    filter_types:   list[str] | None,
    filter_name:    str,
) -> None:
    if filter_domains and cur.get("domain") not in filter_domains:
        return
    if filter_types and cur.get("type") not in filter_types:
        return
    if filter_name and cur.get("name") != filter_name:
        return
    modules.append(dict(cur))


# ---------------------------------------------------------------------------
# Git helpers
# ---------------------------------------------------------------------------

def repo_dir(mod: dict) -> str:
    return os.path.join(REPO_ROOT, mod["name"])


def is_cloned(mod: dict) -> bool:
    return os.path.isdir(os.path.join(repo_dir(mod), ".git"))


def _git(mod: dict, args: list[str], check: bool = True) -> subprocess.CompletedProcess:
    """Run a git command in a module's repo directory."""
    return subprocess.run(
        ["git", "-C", repo_dir(mod)] + args,
        capture_output=True,
        text=True,
        check=check,
    )


def _git_clone(mod: dict, depth: int = 1) -> subprocess.CompletedProcess:
    """Clone a repo into the workspace root."""
    cmd = ["git", "clone"]
    if depth:
        cmd += ["--depth", str(depth)]
    cmd += ["--branch", mod["branch"], mod["url"], repo_dir(mod)]
    return subprocess.run(cmd, capture_output=True, text=True)


# ---------------------------------------------------------------------------
# Output helpers
# ---------------------------------------------------------------------------

SEP = "─" * 60


def _label(mod: dict) -> str:
    return f"{mod['name']}  [{mod['type']}] ({mod['domain']})"


def _print_result(
    symbol: str,
    verb: str,
    mod: dict,
    note: str = "",
) -> None:
    note_str = f"  {dim(note)}" if note else ""
    print(f"  {symbol}  {verb:<8}  {_label(mod)}{note_str}")


def _summary(ok_n: int, skip_n: int, fail_n: int) -> None:
    print(SEP)
    parts = [f"ok: {ok_n}", f"skipped: {skip_n}"]
    if fail_n:
        parts.append(err(f"failed: {fail_n}"))
    print("Done — " + ", ".join(parts))


# ---------------------------------------------------------------------------
# Subcommand: clone
# ---------------------------------------------------------------------------

def cmd_clone(
    modules: list[dict],
    full: bool = False,
    git_name: str = "",
    git_email: str = "",
    dry_run: bool = False,
) -> int:
    depth = 0 if full else 1
    mode = "full history" if full else "shallow (--depth 1)"
    print(f"\nAVM clone — {len(modules)} modules  [{mode}]")
    print(SEP)

    ok_n = skip_n = fail_n = 0

    for mod in modules:
        if is_cloned(mod):
            _print_result(dim("-"), "SKIP", mod, "already cloned")
            skip_n += 1
            # still apply identity config even on skip
            if not dry_run:
                if git_name:
                    _git(mod, ["config", "user.name", git_name], check=False)
                if git_email:
                    _git(mod, ["config", "user.email", git_email], check=False)
            continue

        if dry_run:
            _print_result(dim("~"), "CLONE", mod, "dry-run")
            ok_n += 1
            continue

        print(f"  ↓  CLONE    {_label(mod)}")
        result = _git_clone(mod, depth=depth)
        if result.returncode == 0:
            if git_name:
                _git(mod, ["config", "user.name", git_name], check=False)
            if git_email:
                _git(mod, ["config", "user.email", git_email], check=False)
            ok_n += 1
        else:
            stderr = result.stderr.strip().splitlines()
            note = stderr[-1] if stderr else "clone failed"
            _print_result(err("✗"), "FAIL", mod, note)
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


# ---------------------------------------------------------------------------
# Subcommand: update (pull --ff-only)
# ---------------------------------------------------------------------------

def _do_update(mod: dict, dry_run: bool) -> tuple[str, str, str]:
    """Returns (status, label, note) — runs in a thread."""
    label = _label(mod)
    if not is_cloned(mod):
        return ("skip", label, "not cloned")
    if dry_run:
        return ("dry", label, "")
    result = _git(mod, ["pull", "--ff-only", "--quiet"], check=False)
    if result.returncode == 0:
        return ("ok", label, "")
    note = (result.stderr or result.stdout).strip().splitlines()
    return ("fail", label, note[-1] if note else "pull failed")


def cmd_update(
    modules: list[dict],
    parallel: int = 1,
    dry_run: bool = False,
) -> int:
    cloneable = modules
    print(f"\nAVM update — {len(cloneable)} modules  [parallel: {parallel}]")
    print(SEP)

    ok_n = skip_n = fail_n = 0
    results = _run_parallel(_do_update, cloneable, parallel, dry_run)

    for status, label, note in results:
        if status == "ok":
            print(f"  {ok('✓')}  PULL     {label}")
            ok_n += 1
        elif status == "skip":
            print(f"  {dim('-')}  SKIP     {label}  {dim(note)}")
            skip_n += 1
        elif status == "dry":
            print(f"  {dim('~')}  PULL     {label}  {dim('dry-run')}")
            ok_n += 1
        else:
            print(f"  {err('✗')}  FAIL     {label}  {dim(note)}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


