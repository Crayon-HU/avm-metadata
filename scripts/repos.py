def cmd_branch_create(
    modules: list[dict],
    name: str,
    dry_run: bool = False,
) -> int:
    print(f"\nAVM branch create '{name}' — {len(modules)} modules")
    print(SEP)

    ok_n = skip_n = fail_n = 0
    for mod in modules:
        label = _label(mod)
        if not is_cloned(mod):
            print(f"  {dim('-')}  SKIP     {label}  {dim('not cloned')}")
            skip_n += 1
            continue

        # Check if branch already exists
        check = _git(mod, ["rev-parse", "--verify", name], check=False)
        if check.returncode == 0:
            print(f"  {dim('-')}  EXISTS   {label}  {dim(f'branch {name!r} already exists')}")
            skip_n += 1
            continue

        if dry_run:
            print(f"  {dim('~')}  CREATE   {label}  {dim('dry-run')}")
            ok_n += 1
            continue

        result = _git(mod, ["checkout", "-b", name], check=False)
        if result.returncode == 0:
            print(f"  {ok('✓')}  CREATE   {label}")
            ok_n += 1
        else:
            note = (result.stderr or result.stdout).strip().splitlines()
            print(f"  {err('✗')}  FAIL     {label}  {err(note[-1] if note else 'failed')}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


def cmd_branch_checkout(
    modules: list[dict],
    name: str,
    fallback: bool = False,
    dry_run: bool = False,
) -> int:
    """
    Switch to branch `name`. If the branch doesn't exist:
    - with --fallback: stay on default branch (warn, don't fail)
    - without --fallback: warn and count as failed
    """
    print(f"\nAVM branch checkout '{name}' — {len(modules)} modules")
    print(SEP)

    ok_n = skip_n = fail_n = 0
    for mod in modules:
        label = _label(mod)
        if not is_cloned(mod):
            print(f"  {dim('-')}  SKIP     {label}  {dim('not cloned')}")
            skip_n += 1
            continue

        if dry_run:
            print(f"  {dim('~')}  CHECKOUT {label}  {dim('dry-run')}")
            ok_n += 1
            continue

        result = _git(mod, ["checkout", name], check=False)
        if result.returncode == 0:
            print(f"  {ok('✓')}  CHECKOUT {label}")
            ok_n += 1
        elif fallback:
            print(f"  {warn('~')}  FALLBACK {label}  {warn(f'branch {name!r} not found, staying on current')}")
            skip_n += 1
        else:
            note = (result.stderr or result.stdout).strip().splitlines()
            print(f"  {err('✗')}  FAIL     {label}  {err(note[-1] if note else 'checkout failed')}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


def cmd_branch_delete(
    modules: list[dict],
    name: str,
    force: bool = False,
    dry_run: bool = False,
) -> int:
    flag = "-D" if force else "-d"
    print(f"\nAVM branch delete '{name}' ({flag}) — {len(modules)} modules")
    print(SEP)

    ok_n = skip_n = fail_n = 0
    for mod in modules:
        label = _label(mod)
        if not is_cloned(mod):
            print(f"  {dim('-')}  SKIP     {label}  {dim('not cloned')}")
            skip_n += 1
            continue

        # Check if branch exists
        check = _git(mod, ["rev-parse", "--verify", name], check=False)
        if check.returncode != 0:
            print(f"  {dim('-')}  SKIP     {label}  {dim(f'branch {name!r} not found')}")
            skip_n += 1
            continue

        if dry_run:
            print(f"  {dim('~')}  DELETE   {label}  {dim('dry-run')}")
            ok_n += 1
            continue

        result = _git(mod, ["branch", flag, name], check=False)
        if result.returncode == 0:
            print(f"  {ok('✓')}  DELETE   {label}")
            ok_n += 1
        else:
            note = (result.stderr or result.stdout).strip().splitlines()
            print(f"  {err('✗')}  FAIL     {label}  {err(note[-1] if note else 'delete failed')}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


# ---------------------------------------------------------------------------
# Subcommand: stash / stash pop
# ---------------------------------------------------------------------------

def _cmd_stash_op(
    modules: list[dict],
    op: str,
    dry_run: bool = False,
) -> int:
    """op is 'push' (stash) or 'pop'."""
    verb = "STASH" if op == "push" else "STASH POP"
    print(f"\nAVM {verb.lower()} — {len(modules)} modules")
    print(SEP)

    ok_n = skip_n = fail_n = 0
    for mod in modules:
        label = _label(mod)
        if not is_cloned(mod):
            print(f"  {dim('-')}  SKIP     {label}  {dim('not cloned')}")
            skip_n += 1
            continue
        if dry_run:
            print(f"  {dim('~')}  {verb:<8} {label}  {dim('dry-run')}")
            ok_n += 1
            continue

        git_args = ["stash", op] if op == "pop" else ["stash"]
        result = _git(mod, git_args, check=False)
        if result.returncode == 0:
            note = result.stdout.strip().splitlines()
            print(f"  {ok('✓')}  {verb:<8} {label}  {dim(note[0] if note else '')}")
            ok_n += 1
        else:
            note = (result.stderr or result.stdout).strip().splitlines()
            print(f"  {err('✗')}  FAIL     {label}  {err(note[-1] if note else 'stash failed')}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


def cmd_stash(modules: list[dict], dry_run: bool = False) -> int:
    return _cmd_stash_op(modules, "push", dry_run)


def cmd_stash_pop(modules: list[dict], dry_run: bool = False) -> int:
    return _cmd_stash_op(modules, "pop", dry_run)


