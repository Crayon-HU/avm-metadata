# ---------------------------------------------------------------------------
# Subcommand: run (arbitrary command)
# ---------------------------------------------------------------------------

def cmd_run(
    modules: list[dict],
    cmd_args: list[str],
    parallel: int = 1,
    dry_run: bool = False,
) -> int:
    if not cmd_args:
        _die("'run' requires a command, e.g.: repos.py run git log --oneline -5")

    cmd_str = " ".join(cmd_args)
    print(f"\nAVM run '{cmd_str}' — {len(modules)} modules  [parallel: {parallel}]")
    print(SEP)

    ok_n = fail_n = skip_n = 0

    def _do_run(mod: dict, _dry: bool) -> tuple[str, str, str]:
        label = _label(mod)
        if not is_cloned(mod):
            return ("skip", label, "not cloned")
        if _dry:
            return ("dry", label, "")
        result = subprocess.run(
            cmd_args,
            cwd=repo_dir(mod),
            capture_output=True,
            text=True,
        )
        if result.returncode == 0:
            out = result.stdout.strip()
            return ("ok", label, out)
        note = (result.stderr or result.stdout).strip().splitlines()
        return ("fail", label, note[-1] if note else "command failed")

    results = _run_parallel(_do_run, modules, parallel, dry_run)

    for status, label, note in results:
        if status == "ok":
            print(f"  {ok('✓')}  RUN      {label}")
            if note:
                for line in note.splitlines():
                    print(f"             {dim(line)}")
            ok_n += 1
        elif status in ("skip", "dry"):
            print(f"  {dim('-')}  SKIP     {label}  {dim(note or 'dry-run')}")
            skip_n += 1
        else:
            print(f"  {err('✗')}  FAIL     {label}  {err(note)}")
            fail_n += 1

    _summary(ok_n, skip_n, fail_n)
    return 1 if fail_n else 0


# ---------------------------------------------------------------------------
# Parallelism helper
# ---------------------------------------------------------------------------

def _run_parallel(
    fn,
    modules: list[dict],
    parallel: int,
    dry_run: bool,
) -> list[tuple[str, str, str]]:
    """
    Run fn(mod, dry_run) for each module in a thread pool.
    Returns results in the original module order.
    """
    parallel = max(1, parallel)
    if parallel == 1:
        return [fn(mod, dry_run) for mod in modules]

    results: list[tuple[str, str, str]] = [("", "", "")] * len(modules)
    with ThreadPoolExecutor(max_workers=parallel) as pool:
        future_to_idx = {pool.submit(fn, mod, dry_run): i for i, mod in enumerate(modules)}
        for future in as_completed(future_to_idx):
            idx = future_to_idx[future]
            try:
                results[idx] = future.result()
            except Exception as exc:  # noqa: BLE001
                results[idx] = ("fail", _label(modules[idx]), str(exc))
    return results


